//Creating Class with name KilometersToMilesConversion indicating the purpose is to convert kilometers into miles.
class KilometersToMilesConversion{
    public static void main(String args[]){
         // Declaring variable to store distance in kilometers
         double kilometers = 10.8;

         // Converting kilometers to miles.
         double miles = kilometers * 1.6;

         // Displaying converted distance.
         System.out.println("The distance " + kilometers +  " km in miles is " + miles);
    }
}

